/**
 * claudeCodeBrief.js
 *
 * Builds a structured investigation brief for Claude Code from a Sentinel
 * finding + live snapshot. Writes the brief to ~/.fere/investigation.md,
 * copies it to the clipboard, and opens a Terminal window at the service's
 * project path so the user can immediately run `claude` and paste.
 */

const os = require("os");
const fs = require("fs");
const path = require("path");
const { execFile } = require("child_process");
const { clipboard } = require("electron");

// ─── Brief builder ────────────────────────────────────────────────────────────

function buildBrief(finding, snapshot, containerLogs) {
  const lines = [];
  const now = new Date().toLocaleString();

  lines.push("# Fere Investigation Brief");
  lines.push(`> Generated by Sentinel — ${now}\n`);

  // ── What Sentinel found ──
  lines.push("## Issue\n");
  lines.push(`**[${finding.severity.toUpperCase()}] ${finding.summary}**\n`);
  lines.push(`- **Service:** ${finding.service}`);
  if (finding.detail) lines.push(`- **Detail:** ${finding.detail}`);
  if (finding.impact) lines.push(`- **Impact:** ${finding.impact}`);
  if (finding.affectedServices?.length) {
    lines.push(`- **Downstream affected:** ${finding.affectedServices.join(", ")}`);
  }
  lines.push("");

  // ── Runtime state ──
  const nodes = snapshot?.graph?.nodes ?? [];
  const edges = snapshot?.graph?.edges ?? [];
  const relevant = new Set([finding.service, ...(finding.affectedServices ?? [])]);

  if (nodes.length > 0) {
    lines.push("## Runtime State (verified by Sentinel)\n");
    const nodeMap = new Map(nodes.map((n) => [n.id, n]));

    for (const node of nodes) {
      const name = node.name || node.label || node.service || "";
      if (!relevant.has(name)) continue;

      const health = node.health ?? "unknown";
      const port = node.ports?.[0]?.port;
      const projPath = node.projectPath;

      lines.push(
        `- **${name}**: ${health}` +
          (port ? ` · port ${port}` : "") +
          (projPath ? ` · \`${projPath}\`` : ""),
      );

      const outgoing = edges.filter((e) => e.source === node.id);
      const incoming = edges.filter((e) => e.target === node.id);

      if (outgoing.length) {
        const names = outgoing
          .map((e) => {
            const t = nodeMap.get(e.target);
            return t ? t.name || t.label || t.id : e.target;
          })
          .join(", ");
        lines.push(`  - Calls: ${names}`);
      }
      if (incoming.length) {
        const names = incoming
          .map((e) => {
            const s = nodeMap.get(e.source);
            return s ? s.name || s.label || s.id : e.source;
          })
          .join(", ");
        lines.push(`  - Called by: ${names}`);
      }
    }
    lines.push("");
  }

  // ── Logs ──
  if (containerLogs) {
    lines.push("## Recent Logs\n");
    lines.push("```");
    // Trim to last ~80 lines to stay focused
    const logLines = containerLogs.split("\n");
    const tail = logLines.slice(-80).join("\n");
    lines.push(tail);
    lines.push("```\n");
  }

  // ── Project paths ──
  const projectPaths = new Set();
  for (const node of nodes) {
    const name = node.name || node.label || node.service || "";
    if (relevant.has(name) && node.projectPath) {
      projectPaths.add(node.projectPath);
    }
  }

  if (projectPaths.size > 0) {
    lines.push("## Files to Check\n");
    for (const p of projectPaths) {
      lines.push(`- \`${p}\``);
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("*Investigate the root cause and suggest a concrete fix.*");

  return lines.join("\n");
}

// ─── Terminal launcher ────────────────────────────────────────────────────────

function openTerminalAt(dirPath) {
  const escaped = dirPath.replace(/\\/g, "\\\\").replace(/'/g, "'\\''");
  const script = `tell application "Terminal"
  activate
  do script "cd '${escaped}' && claude"
end tell`;
  return new Promise((resolve) => {
    execFile("osascript", ["-e", script], () => resolve());
  });
}

// ─── Main export ─────────────────────────────────────────────────────────────

/**
 * Build an investigation brief for a Sentinel finding and hand it off to
 * Claude Code.
 *
 * @param {import('../../src/types/electron').AgentFinding} finding
 * @param {object} snapshot  — snapshotScheduler.previousSnapshot
 * @param {string|null}  containerLogs  — raw log output for the primary service
 * @returns {{ success: boolean, briefPath: string, projectPath: string, error?: string }}
 */
async function openInClaudeCode(finding, snapshot, containerLogs) {
  try {
    const briefContent = buildBrief(finding, snapshot, containerLogs);

    // Write to ~/.fere/investigation.md
    const fereDir = path.join(os.homedir(), ".fere");
    fs.mkdirSync(fereDir, { recursive: true });
    const briefPath = path.join(fereDir, "investigation.md");
    fs.writeFileSync(briefPath, briefContent, "utf8");

    // Copy to clipboard so the user can paste into `claude`
    clipboard.writeText(briefContent);

    // Resolve project path for the primary service
    let projectPath = os.homedir();
    const nodes = snapshot?.graph?.nodes ?? [];
    for (const node of nodes) {
      const name = node.name || node.label || node.service || "";
      if (name === finding.service && node.projectPath) {
        projectPath = node.projectPath;
        break;
      }
    }

    // Open Terminal at the project directory
    await openTerminalAt(projectPath);

    return { success: true, briefPath, projectPath };
  } catch (err) {
    return { success: false, briefPath: "", projectPath: "", error: err.message };
  }
}

module.exports = { openInClaudeCode, buildBrief };
